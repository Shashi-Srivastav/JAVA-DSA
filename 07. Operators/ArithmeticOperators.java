import java.util.*;
public class ArithmeticOperators{
    public static void main(String args[]){
        int a = 5;
        int b = 9;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}