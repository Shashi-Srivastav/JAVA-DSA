import java.util.*;
public class TypeCasting {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        float num = 99.99f;
        int num1 = (int)num;
        System.out.println(num1);
        char ch = 'a';
        int number = ch;
        System.out.println(number);
    }
}
