import java.util.*;

public class Switch_case {
    /**
     * @param args
     */
    public static void main(String args[]){
        int num = 2;
        switch(num){
        case 1: System.out.println("1");
            break;
        case 2: System.out.println("2");
            break;
        default : System.out.println("3");
        }
    }
}
